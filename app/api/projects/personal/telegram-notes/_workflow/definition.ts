// node:fetch-telegram-updates — Reception from the @fractera_auto listener [step]
// Step 201 (agent-channel-routing.md §3): reception is now delivered by the substrate
// fractera-automations listener, NOT self-polled. This automation MUST NOT call getUpdates
// itself — a second consumer of the bot re-creates the message-stealing collision (the whole
// reason the listener exists). Input arrives as the listener's JSON envelope; a plain string
// remains supported as the run-panel manual test (simulateMessage).
async function fetchTelegramUpdates(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const allowedChat = process.env.TELEGRAM_ALLOWED_CHAT_ID ?? "";
  const raw = typeof artifacts.input === "string" ? (artifacts.input as string).trim() : "";

  // Empty input = a scheduled /run tick (reminder delivery) or a bare trigger — NO message to
  // process, and we never poll. deliver-due-reminders still runs on the tick.
  if (!raw) return [] satisfies TgMessage[];

  // Listener envelope: the fractera-automations service (step 205) forwards EVERY message from this
  // automation's own bot here — no hook matching; the bot identity already selected this automation.
  try {
    const env = JSON.parse(raw) as {
      source?: string; kind?: string; data?: unknown; callbackQueryId?: unknown;
      chatId?: unknown; messageId?: unknown; text?: unknown; date?: unknown; photoFileId?: unknown;
    };
    // Callback from an inline button (step 205 §D): the user picked an action for an earlier "unclear"
    // message. Resolve the pending text + forced action, ack the tap, and feed it back into the pipeline.
    if (env && env.source === "telegram" && env.kind === "callback" && typeof env.data === "string") {
      const cbChat = String(env.chatId ?? "");
      if (allowedChat && cbChat && cbChat !== allowedChat) return [] satisfies TgMessage[];
      await tgAnswerCallback(String(env.callbackQueryId ?? ""));
      // Duplicate-confirmation callbacks (step 207.x): reuse this same confirmation node. dupkeep =
      // keep both (the record is already saved); dupdel:<table>:<id> = remove the just-created dup.
      const cbChatFinal = cbChat || allowedChat || "0";
      if (env.data === "dupkeep") {
        await sendLocalized(cbChatFinal, "Kept both records.");
        return [] satisfies TgMessage[];
      }
      if (env.data.startsWith("dupdel:")) {
        const parts = env.data.split(":");
        await removeDuplicateRecord(parts[1] ?? "", Number(parts[2]), cbChatFinal);
        return [] satisfies TgMessage[];
      }
      // Reconciliation-wizard callbacks (step 207.10 P2): period → categories(toggle) → confirm → compute.
      // Every step is a tap handled here and short-circuits the pipeline (returns no message to classify).
      if (env.data.startsWith("recon:")) {
        const parts = env.data.split(":"); // recon:period:7 | recon:cat:food | recon:catall | recon:catdone | recon:go | recon:cancel
        const sub = parts[1] ?? "";
        if (sub === "cancel") { await clearRecon(cbChatFinal); await sendLocalized(cbChatFinal, "Okay, cancelled."); return [] satisfies TgMessage[]; }
        const st = await loadRecon(cbChatFinal);
        if (!st) { await clearRecon(cbChatFinal); await sendLocalized(cbChatFinal, "This dialog has expired — ask your money question again."); return [] satisfies TgMessage[]; }
        if (sub === "period") {
          const days = Number(parts[2] ?? 0);
          st.sinceDays = days > 0 ? days : null;
          st.stage = "categories";
          await saveRecon(cbChatFinal, st);
          await reconAskCategories(cbChatFinal, st);
        } else if (sub === "cat") {
          const id = parts[2] ?? "";
          if (id) { const i = st.categories.indexOf(id); if (i >= 0) st.categories.splice(i, 1); else st.categories.push(id); await saveRecon(cbChatFinal, st); }
          await reconAskCategories(cbChatFinal, st);
        } else if (sub === "catall") {
          st.categories = [];
          await reconConfirm(cbChatFinal, st);
        } else if (sub === "catdone") {
          await reconConfirm(cbChatFinal, st);
        } else if (sub === "go") {
          await reconCompute(cbChatFinal, st);
        }
        return [] satisfies TgMessage[];
      }
      const sep = env.data.indexOf(":");
      const pid = sep >= 0 ? env.data.slice(0, sep) : "";
      const action = sep >= 0 ? env.data.slice(sep + 1) : "ignore";
      const pending = await loadPending(pid);
      if (!pending) return [] satisfies TgMessage[];
      const act = (["save", "remind", "recall", "ignore"].includes(action) ? action : "ignore") as Intent;
      if (act === "ignore") {
        await sendLocalized(pending.chatId, "Okay — ignored.");
        return [] satisfies TgMessage[];
      }
      return [
        { chatId: pending.chatId, messageId: 0, text: pending.text, date: Math.floor(Date.now() / 1000), forcedAction: act },
      ] satisfies TgMessage[];
    }
    if (env && env.source === "telegram" && (typeof env.text === "string" || typeof env.photoFileId === "string")) {
      const chatId = String(env.chatId ?? "");
      if (allowedChat && chatId && chatId !== allowedChat) return [] satisfies TgMessage[];
      const finalChat = chatId || allowedChat || "0";
      // Hard idempotency (step 207.x, Tier 1): the same Telegram message re-delivered (process restart
      // mid-ack, retry, double dispatch) must never insert twice — skip it silently.
      if (await markProcessedOrSkip(finalChat, Number(env.messageId ?? 0))) return [] satisfies TgMessage[];
      // Remember the owner's chat so the "Test bot" button can send them a message (a bot cannot
      // initiate a chat — it needs a chat the owner has already talked to). Best-effort (step 205).
      try {
        db.prepare(
          "INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES ('last_chat_id', ?)",
        ).run(finalChat);
      } catch { /* state table optional */ }
      return [
        {
          chatId: finalChat,
          messageId: Number(env.messageId ?? 0),
          text: typeof env.text === "string" ? env.text : "",
          date: Number(env.date ?? Math.floor(Date.now() / 1000)),
          photoFileId: typeof env.photoFileId === "string" ? env.photoFileId : undefined,
        },
      ] satisfies TgMessage[];
    }
  } catch {
    // not JSON → fall through to the plain-string manual-test branch
  }

  // Run-panel manual test branch: a plain string is a single message from the allowed chat.
  return [
    { chatId: allowedChat || "0", messageId: 0, text: raw, date: Math.floor(Date.now() / 1000) },
  ] satisfies TgMessage[];
}

// node:deliver-due-reminders — Deliver due reminders (date push) [step]
async function deliverDueReminders(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";
  void artifacts;

  const now = Math.floor(Date.now() / 1000);
  const due = (await db
    .prepare(
      "SELECT id, chat_id, reminder_due, full_text, summary FROM telegram_notes " +
        "WHERE reminder_due IS NOT NULL AND reminder_due <= ? AND delivered = 0",
    )
    .all(now)) as Array<{ id: number; chat_id: string; reminder_due: number; full_text: string; summary: string }>;

  const errors: string[] = [];
  let delivered = 0;
  for (const row of due) {
    const when = new Date(row.reminder_due * 1000);
    const hhmm = `${String(when.getHours()).padStart(2, "0")}:${String(when.getMinutes()).padStart(2, "0")}`;
    const what = row.summary || row.full_text;
    try {
      await sendLocalized(row.chat_id, `Reminder: you asked to be reminded at ${hhmm} to ${what}`);
      await db.prepare("UPDATE telegram_notes SET delivered = 1 WHERE id = ?").run(row.id);
      delivered++;
    } catch (e) {
      errors.push(`reminder ${row.id}: ${e instanceof Error ? e.message : String(e)}`);
    }
  }
  return { delivered, errors };
}

// node:classify-message — Classify the message into one of this automation's actions [router]
// Step 205 (§D, agent-channel-routing.md §3.1): this automation owns its Telegram bot, so there is
// NO cross-automation hook registry. Each message is classified into one of THIS automation's actions
// with the per-project cheap model; clear → that action, unclear → ignore for now (the inline-button
// fallback is step 205.10). payload = the whole message (there is no hook phrase to strip).
async function classifyMessage(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const messages = ((artifacts["fetch-telegram-updates"] as TgMessage[]) ?? []);
  if (!messages.length) return [] satisfies Classified2[];

  const out: Classified2[] = [];
  for (const msg of messages) {
    // Language (step 207.10 P1): detect + remember the chat's language from any text message so every
    // downstream reply answers in it. Cyrillic is a no-model fast path; other scripts ask the model once.
    if (msg.text && msg.text.trim()) await resolveLang(msg.chatId, msg.text);
    // A button tap (callback) carries a forced action — skip the model, honor the user's choice.
    if (msg.forcedAction) {
      out.push({ intent: msg.forcedAction, projectSlug: PROJECT_SLUG, payload: msg.text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date });
      continue;
    }
    // A photo always goes to document digitization (step 205 §E): a receipt / document → finance record.
    if (msg.photoFileId) {
      out.push({ intent: "finance", projectSlug: PROJECT_SLUG, payload: msg.text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date, photoFileId: msg.photoFileId });
      continue;
    }
    // A slash command (the bot menu, step 205 §I) maps deterministically — no model call.
    const slash = parseSlashCommand(msg.text);
    if (slash) {
      out.push({ intent: slash.intent, projectSlug: PROJECT_SLUG, payload: slash.payload, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date });
      continue;
    }
    const text = msg.text.trim();
    let intent: Intent = "ignore";
    let unclear = false;
    if (text) {
      const ans = await cheapModel(
        "You route one message of a personal-notes assistant to a single action. Reply with ONLY one word: " +
          "save (states a fact/note to remember), remind (wants a time-based reminder), " +
          "recall (asks to find/retrieve something saved), finance (a money movement to RECORD — income or expense, " +
          "e.g. 'got paid 1000', 'bought ice cream for 2 euro'), finance-query (a QUESTION about money already " +
          "recorded — how much spent/earned, e.g. 'how much did I spend on food this week'), " +
          "photo (asks to SEE / send back a previously saved receipt photo or image), " +
          "meta (a conversational message, a complaint, a follow-up ABOUT the assistant or a previous answer, or a " +
          "request about how the bot works — e.g. 'answer in Russian', 'did you save it?', 'why didn't you reply', " +
          "'what is it called in the database'), " +
          "escalate (the user wants a NEW capability this assistant does NOT have, or is dissatisfied it cannot do " +
          "something — a feature request / 'can you also do X' / 'this should also…' / 'I want it to…' beyond notes, " +
          "reminders and finance), " +
          "or unclear (a real note/request whose action is genuinely ambiguous). No other text.",
        text.slice(0, 1000),
        6,
      );
      // finance-query BEFORE finance so the longer token wins the match.
      let word = (ans.toLowerCase().match(/save|remind|recall|finance-query|finance|escalate|meta|photo|unclear/) ?? [""])[0];
      // Heuristic fallback (step 207.16): an OpenAI outage used to turn EVERY message — including a
      // crystal-clear "запомни…" — into the unclear-buttons picker (owner test, 19:57–23:01). When the
      // model failed ("" = API error) or shrugged, route obvious phrasings by keyword instead; only a
      // genuinely ambiguous message still gets the picker. Works with the key down (self-sufficiency).
      if (!word || word === "unclear") {
        const h = heuristicIntent(text);
        if (h) word = h;
      }
      if (!word || word === "unclear") { intent = "ignore"; unclear = true; }
      else intent = word as Intent;
    }
    out.push({
      intent,
      projectSlug: PROJECT_SLUG,
      // unclear keeps the text so reply-in-telegram can offer buttons; a plain empty message stays "".
      payload: intent === "ignore" ? (unclear ? text : "") : text,
      hookPhrase: "",
      unclear,
      chatId: msg.chatId,
      messageId: msg.messageId,
      date: msg.date,
    });
  }
  return out;
}

