async function tgSend(chatId: string, text: string): Promise<void> {
  // Telegram caps messages at 4096 chars — split on paragraph/sentence boundaries.
  const chunks: string[] = [];
  let rest = text;
  while (rest.length > 4096) {
    let cut = rest.lastIndexOf("\n", 4096);
    if (cut < 2000) cut = rest.lastIndexOf(". ", 4096);
    if (cut < 2000) cut = 4096;
    chunks.push(rest.slice(0, cut));
    rest = rest.slice(cut);
  }
  chunks.push(rest);
  for (const chunk of chunks) {
    await fetch(`${TG_API}/bot${botToken()}/sendMessage`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      // plain text on purpose — free-form notes must not break on Markdown specials.
      body: JSON.stringify({ chat_id: chatId, text: chunk }),
    }).catch(() => undefined);
  }
}

