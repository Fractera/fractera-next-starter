async function tgSend(chatId: string, text: string): Promise<void> {
  // Telegram caps messages at 4096 chars — split on paragraph/sentence boundaries.
  const chunks: string[] = [];
  let rest = text;
  while (rest.length > 4096) {
    let cut = rest.lastIndexOf("\n", 4096);
    if (cut < 2000) cut = rest.lastIndexOf(". ", 4096);
    if (cut < 2000) cut = 4096;
    chunks.push(rest.slice(0, cut));
    rest = rest.slice(cut);
  }
  chunks.push(rest);
  for (const chunk of chunks) {
    await fetch(`${TG_API}/bot${botToken()}/sendMessage`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      // plain text on purpose — free-form notes must not break on Markdown specials.
      body: JSON.stringify({ chat_id: chatId, text: chunk }),
    }).catch(() => undefined);
  }
  // Conversation history (step 207.16): every outgoing reply is the bot's side of the thread. tgSend is
  // the choke point for all text replies (sendLocalized routes here too); button prompts are not recorded.
  await appendHistory(chatId, "bot", text);
}

// Send a stored receipt image back to the chat (step 207.10). The media URL may be absolute or a relative
// app path (/api/media/…); relative paths are resolved against the app base. We fetch the bytes and upload
// them as multipart so Telegram never has to reach an internal/relative URL itself. Best-effort → boolean.
const APP_BASE = (process.env.APP_BASE_URL ?? "http://localhost:3000").replace(/\/+$/, "");
async function tgSendPhotoByUrl(chatId: string, imageUrl: string, caption: string): Promise<boolean> {
  try {
    const abs = /^https?:\/\//.test(imageUrl)
      ? imageUrl
      : `${APP_BASE}${imageUrl.startsWith("/") ? "" : "/"}${imageUrl}`;
    const img = await fetch(abs, { signal: AbortSignal.timeout(30000) });
    if (!img.ok) return false;
    const bytes = new Uint8Array(await img.arrayBuffer());
    const ab = bytes.buffer.slice(bytes.byteOffset, bytes.byteOffset + bytes.byteLength) as ArrayBuffer;
    const form = new FormData();
    form.append("chat_id", chatId);
    if (caption) form.append("caption", caption.slice(0, 1024));
    form.append("photo", new Blob([ab], { type: "image/jpeg" }), "record.jpg");
    const r = await fetch(`${TG_API}/bot${botToken()}/sendPhoto`, { method: "POST", body: form, signal: AbortSignal.timeout(30000) });
    return r.ok;
  } catch {
    return false;
  }
}

// Model-compatible completion params (step 207.16 round 4 root cause): the owner set
// TELEGRAM_NOTES_MODEL=gpt-5.5 — the gpt-5 family REJECTS `max_tokens` (wants
// `max_completion_tokens`) and a non-default `temperature`, so EVERY call silently 400'd
// and the whole automation degraded to pickers/English. `max_completion_tokens` is the
// canonical name accepted by current models; reasoning models also burn thinking tokens
// out of the same budget, so tiny caps (classify=6) must be floored or the answer arrives
// empty. Keep temperature:0 only where it is supported (gpt-4o family).
function isReasoningModel(model: string): boolean {
  return /^(gpt-5|o\d)/i.test(model);
}
function completionParams(model: string, maxTokens: number): Record<string, unknown> {
  return isReasoningModel(model)
    ? { max_completion_tokens: Math.max(maxTokens, 512) }
    : { temperature: 0, max_completion_tokens: maxTokens };
}

// One cheap-model chat completion. Returns the assistant text (trimmed) or "" on failure —
// callers degrade gracefully (the automation never dies because the model hiccuped).
async function cheapModel(system: string, user: string, maxTokens = 400): Promise<string> {
  // OPENAI_KEY() — NOT a direct process.env read: the step context may lack the runtime-loaded env
  // (step 207.16 round 2 root cause — this line read env directly and silently returned "").
  const key = OPENAI_KEY();
  if (!key) return "";
  try {
    const r = await fetch(OPENAI_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: `Bearer ${key}` },
      body: JSON.stringify({
        model: CHEAP_MODEL,
        messages: [
          { role: "system", content: system },
          { role: "user", content: user },
        ],
        ...completionParams(CHEAP_MODEL, maxTokens),
      }),
      signal: AbortSignal.timeout(60_000),
    });
    if (!r.ok) return "";
    const data = (await r.json()) as { choices?: Array<{ message?: { content?: string } }> };
    return (data.choices?.[0]?.message?.content ?? "").trim();
  } catch {
    return "";
  }
}

