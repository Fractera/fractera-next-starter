// node:fetch-telegram-updates — Reception from the @fractera_auto listener [step]
// Step 201 (agent-channel-routing.md §3): reception is now delivered by the substrate
// fractera-automations listener, NOT self-polled. This automation MUST NOT call getUpdates
// itself — a second consumer of the bot re-creates the message-stealing collision (the whole
// reason the listener exists). Input arrives as the listener's JSON envelope; a plain string
// remains supported as the run-panel manual test (simulateMessage).
async function fetchTelegramUpdates(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const allowedChat = process.env.TELEGRAM_ALLOWED_CHAT_ID ?? "";
  const raw = typeof artifacts.input === "string" ? (artifacts.input as string).trim() : "";

  // Empty input = a scheduled /run tick (reminder delivery) or a bare trigger — NO message to
  // process, and we never poll. deliver-due-reminders still runs on the tick.
  if (!raw) return [] satisfies TgMessage[];

  // Listener envelope: the fractera-automations service (step 205) forwards EVERY message from this
  // automation's own bot here — no hook matching; the bot identity already selected this automation.
  try {
    const env = JSON.parse(raw) as {
      source?: string; kind?: string; data?: unknown; callbackQueryId?: unknown;
      chatId?: unknown; messageId?: unknown; text?: unknown; date?: unknown; photoFileId?: unknown;
      location?: { lat?: unknown; lng?: unknown; title?: unknown };
    };
    // Callback from an inline button (step 205 §D): the user picked an action for an earlier "unclear"
    // message. Resolve the pending text + forced action, ack the tap, and feed it back into the pipeline.
    if (env && env.source === "telegram" && env.kind === "callback" && typeof env.data === "string") {
      const cbChat = String(env.chatId ?? "");
      if (allowedChat && cbChat && cbChat !== allowedChat) return [] satisfies TgMessage[];
      await tgAnswerCallback(String(env.callbackQueryId ?? ""));
      // Duplicate-confirmation callbacks (step 207.x): reuse this same confirmation node. dupkeep =
      // keep both (the record is already saved); dupdel:<table>:<id> = remove the just-created dup.
      const cbChatFinal = cbChat || allowedChat || "0";
      if (env.data === "dupkeep") {
        await sendLocalized(cbChatFinal, "Kept both records.");
        return [] satisfies TgMessage[];
      }
      if (env.data.startsWith("dupdel:")) {
        const parts = env.data.split(":");
        await removeDuplicateRecord(parts[1] ?? "", Number(parts[2]), cbChatFinal);
        return [] satisfies TgMessage[];
      }
      // Confirmed deletion (step 207.18b): deldo:<specId> → delete every target pair (row + vector doc
      // + image links); delcancel → nothing touched. The spec was stored by the delete prompt.
      if (env.data === "delcancel") {
        await sendLocalized(cbChatFinal, "Okay — nothing deleted.");
        return [] satisfies TgMessage[];
      }
      if (env.data.startsWith("deldo:")) {
        const specId = env.data.slice("deldo:".length);
        try {
          const row = (await db.prepare("SELECT value FROM telegram_notes_state WHERE key = ?").get(`delspec:${specId}`)) as { value?: string } | null;
          if (!row?.value) {
            await sendLocalized(cbChatFinal, "This deletion request has expired — ask again.");
            return [] satisfies TgMessage[];
          }
          const spec = JSON.parse(row.value) as { targets: Array<{ kind: "note" | "finance"; id: number }> };
          let done = 0;
          for (const t of spec.targets) {
            try { await deleteRecordPair(t.kind, t.id); done++; } catch { /* per-record best-effort */ }
          }
          await db.prepare("DELETE FROM telegram_notes_state WHERE key = ?").run(`delspec:${specId}`);
          await sendLocalized(cbChatFinal, done === 1 ? "Deleted 1 record (and its memory entry)." : `Deleted ${done} records (and their memory entries).`);
        } catch {
          await sendLocalized(cbChatFinal, "I couldn't delete those records just now.");
        }
        return [] satisfies TgMessage[];
      }
      // Reconciliation-wizard callbacks (step 207.10 P2): period → categories(toggle) → confirm → compute.
      // Every step is a tap handled here and short-circuits the pipeline (returns no message to classify).
      if (env.data.startsWith("recon:")) {
        const parts = env.data.split(":"); // recon:period:7 | recon:cat:food | recon:catall | recon:catdone | recon:go | recon:cancel
        const sub = parts[1] ?? "";
        if (sub === "cancel") { await clearRecon(cbChatFinal); await sendLocalized(cbChatFinal, "Okay, cancelled."); return [] satisfies TgMessage[]; }
        const st = await loadRecon(cbChatFinal);
        if (!st) { await clearRecon(cbChatFinal); await sendLocalized(cbChatFinal, "This dialog has expired — ask your money question again."); return [] satisfies TgMessage[]; }
        if (sub === "period") {
          const days = Number(parts[2] ?? 0);
          st.sinceDays = days > 0 ? days : null;
          st.stage = "categories";
          await saveRecon(cbChatFinal, st);
          await reconAskCategories(cbChatFinal, st);
        } else if (sub === "cat") {
          const id = parts[2] ?? "";
          if (id) { const i = st.categories.indexOf(id); if (i >= 0) st.categories.splice(i, 1); else st.categories.push(id); await saveRecon(cbChatFinal, st); }
          await reconAskCategories(cbChatFinal, st);
        } else if (sub === "catall") {
          st.categories = [];
          await reconConfirm(cbChatFinal, st);
        } else if (sub === "catdone") {
          await reconConfirm(cbChatFinal, st);
        } else if (sub === "go") {
          await reconCompute(cbChatFinal, st);
        }
        return [] satisfies TgMessage[];
      }
      const sep = env.data.indexOf(":");
      const pid = sep >= 0 ? env.data.slice(0, sep) : "";
      const action = sep >= 0 ? env.data.slice(sep + 1) : "ignore";
      const pending = await loadPending(pid);
      if (!pending) return [] satisfies TgMessage[];
      const act = (["save", "remind", "recall", "ignore"].includes(action) ? action : "ignore") as Intent;
      if (act === "ignore") {
        await sendLocalized(pending.chatId, "Okay — ignored.");
        return [] satisfies TgMessage[];
      }
      return [
        { chatId: pending.chatId, messageId: 0, text: pending.text, date: Math.floor(Date.now() / 1000), forcedAction: act },
      ] satisfies TgMessage[];
    }
    if (env && env.source === "telegram" && (typeof env.text === "string" || typeof env.photoFileId === "string" || typeof env.location?.lat === "number")) {
      const chatId = String(env.chatId ?? "");
      if (allowedChat && chatId && chatId !== allowedChat) return [] satisfies TgMessage[];
      const finalChat = chatId || allowedChat || "0";
      // Hard idempotency (step 207.x, Tier 1): the same Telegram message re-delivered (process restart
      // mid-ack, retry, double dispatch) must never insert twice — skip it silently.
      if (await markProcessedOrSkip(finalChat, Number(env.messageId ?? 0))) return [] satisfies TgMessage[];
      // Remember the owner's chat so the "Test bot" button can send them a message (a bot cannot
      // initiate a chat — it needs a chat the owner has already talked to). Best-effort (step 205).
      try {
        db.prepare(
          "INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES ('last_chat_id', ?)",
        ).run(finalChat);
      } catch { /* state table optional */ }
      return [
        {
          chatId: finalChat,
          messageId: Number(env.messageId ?? 0),
          text: typeof env.text === "string" ? env.text : "",
          date: Number(env.date ?? Math.floor(Date.now() / 1000)),
          photoFileId: typeof env.photoFileId === "string" ? env.photoFileId : undefined,
          // Geo-mark (step 207.20): a shared location/venue rides along; classify registers it.
          location: typeof env.location?.lat === "number" && typeof env.location?.lng === "number"
            ? { lat: env.location.lat, lng: env.location.lng, title: typeof env.location.title === "string" ? env.location.title : "" }
            : undefined,
        },
      ] satisfies TgMessage[];
    }
  } catch {
    // not JSON → fall through to the plain-string manual-test branch
  }

  // Run-panel manual test branch: a plain string is a single message from the allowed chat.
  return [
    { chatId: allowedChat || "0", messageId: 0, text: raw, date: Math.floor(Date.now() / 1000) },
  ] satisfies TgMessage[];
}

// node:deliver-due-reminders — Deliver due reminders (date push) [step]
async function deliverDueReminders(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";
  void artifacts;

  const now = Math.floor(Date.now() / 1000);
  const due = (await db
    .prepare(
      "SELECT id, chat_id, reminder_due, full_text, summary FROM telegram_notes " +
        "WHERE reminder_due IS NOT NULL AND reminder_due <= ? AND delivered = 0",
    )
    .all(now)) as Array<{ id: number; chat_id: string; reminder_due: number; full_text: string; summary: string }>;

  const errors: string[] = [];
  let delivered = 0;
  for (const row of due) {
    const when = new Date(row.reminder_due * 1000);
    const hhmm = `${String(when.getHours()).padStart(2, "0")}:${String(when.getMinutes()).padStart(2, "0")}`;
    const what = row.summary || row.full_text;
    try {
      await sendLocalized(row.chat_id, `Reminder: you asked to be reminded at ${hhmm} to ${what}`);
      // Reminder with a place (step 207.20): «напомни сюда сходить в субботу» — the linked geo-mark
      // follows as a tappable maps link, sent RAW (a URL must never pass through the translator).
      const geos = await geoOfRecord("note", row.id);
      if (geos.length) await tgSend(row.chat_id, geoLines(geos.slice(0, 2)));
      await db.prepare("UPDATE telegram_notes SET delivered = 1 WHERE id = ?").run(row.id);
      delivered++;
    } catch (e) {
      errors.push(`reminder ${row.id}: ${e instanceof Error ? e.message : String(e)}`);
    }
  }
  return { delivered, errors };
}

// node:classify-message — Classify the message into one of this automation's actions [router]
// Step 205 (§D, agent-channel-routing.md §3.1): this automation owns its Telegram bot, so there is
// NO cross-automation hook registry. Each message is classified into one of THIS automation's actions
// with the per-project cheap model; clear → that action, unclear → ignore for now (the inline-button
// fallback is step 205.10). payload = the whole message (there is no hook phrase to strip).
async function classifyMessage(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const messages = ((artifacts["fetch-telegram-updates"] as TgMessage[]) ?? []);
  if (!messages.length) return [] satisfies Classified2[];

  const out: Classified2[] = [];
  for (const msg of messages) {
    // Language (step 207.10 P1): detect + remember the chat's language from any text message so every
    // downstream reply answers in it. Cyrillic is a no-model fast path; other scripts ask the model once.
    if (msg.text && msg.text.trim()) await resolveLang(msg.chatId, msg.text);
    // Geo intake (step 207.20): a shared location registers as a pending geo-mark immediately. A
    // location-ONLY message (no text, no photo) is complete here: it late-attaches to the chat's
    // latest geo-less record (mirror of a bare photo) or stays pending for the next record.
    if (msg.location) {
      const geoId = await registerGeo(msg.chatId, msg.location.lat, msg.location.lng, msg.location.title || msg.text.trim(), "telegram-location");
      if (!msg.text.trim() && !msg.photoFileId) {
        if (geoId != null) {
          const attachedTo = await lateAttachGeo(msg.chatId, { id: geoId, lat: msg.location.lat, lng: msg.location.lng, label: msg.location.title });
          await sendLocalized(
            msg.chatId,
            attachedTo
              ? `Location attached to your record: ${attachedTo}`
              : "Location saved — I'll attach it to your next note or record.",
          );
        }
        continue;
      }
    }
    // Maps link in the text (step 207.20): register the place, then classify the text WITHOUT the URL
    // (the words are the note; the link is its geo-mark).
    if (msg.text && MAPS_URL_RE.test(msg.text)) {
      const found = await extractMapsLink(msg.text);
      if (found) {
        const geoId = await registerGeo(msg.chatId, found.lat, found.lng, found.cleaned.slice(0, 100), "maps-link");
        msg.text = found.cleaned;
        // A link-only message is complete here — same late-attach as a bare location.
        if (!msg.text && !msg.photoFileId) {
          const attachedTo = geoId != null
            ? await lateAttachGeo(msg.chatId, { id: geoId, lat: found.lat, lng: found.lng, label: "" })
            : null;
          await sendLocalized(
            msg.chatId,
            attachedTo
              ? `Location attached to your record: ${attachedTo}`
              : "Location saved — I'll attach it to your next note or record.",
          );
          continue;
        }
      }
    }
    // A button tap (callback) carries a forced action — skip the model, honor the user's choice.
    if (msg.forcedAction) {
      out.push({ intent: msg.forcedAction, projectSlug: PROJECT_SLUG, payload: msg.text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date });
      continue;
    }
    // A photo WITHOUT a caption goes to digitization (a receipt attempt; a non-receipt is registered
    // pending — rule R2). A photo WITH a caption falls through: the caption is classified like any
    // text (step 207.18 — «это моя резиденция, запомни» must become a NOTE with the photo linked),
    // and the photoFileId rides along so parse-document registers the image whatever the intent.
    if (msg.photoFileId && !msg.text.trim()) {
      out.push({ intent: "finance", projectSlug: PROJECT_SLUG, payload: msg.text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date, photoFileId: msg.photoFileId });
      continue;
    }
    // A slash command (the bot menu, step 205 §I) maps deterministically — no model call.
    const slash = parseSlashCommand(msg.text);
    if (slash) {
      out.push({ intent: slash.intent, projectSlug: PROJECT_SLUG, payload: slash.payload, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date });
      continue;
    }
    const text = msg.text.trim();
    let intent: Intent = "ignore";
    let unclear = false;
    if (text) {
      // Conversation history (step 207.16, owner decision): load the thread BEFORE recording the new
      // message, feed it to the classifier so follow-ups («а повтори на русском ответ») route correctly.
      // Cost discipline: an unambiguous save-prefix skips the model call entirely.
      const hist = await loadHistory(msg.chatId);
      await appendHistory(msg.chatId, "user", text);
      const pre = /^\s*(запомни|запиши|сохрани|remember)/i.test(text) ? "save" : null;
      const ans = pre ?? await cheapModel(
        "You route the NEW MESSAGE of a personal-notes assistant to a single action. Recent conversation " +
          "may be included for context — classify ONLY the NEW MESSAGE; a follow-up about a previous " +
          "answer (asking to repeat / translate / clarify it) = meta. Reply with ONLY one word: " +
          "save (states a fact/note to remember), remind (wants a time-based reminder), " +
          "recall (asks to find/retrieve something saved), finance (a money movement to RECORD — income or expense, " +
          "e.g. 'got paid 1000', 'bought ice cream for 2 euro'), finance-query (a QUESTION about money already " +
          "recorded — how much spent/earned, e.g. 'how much did I spend on food this week'), " +
          "photo (asks to SEE / send back a previously saved receipt photo or image), " +
          "delete (asks to REMOVE / erase previously recorded records — «убери», «удали запись», " +
          "'delete all 11-euro records' — NEVER record anything for these), " +
          "compound (ONE message that carries BOTH a note-worthy experience AND a money movement — e.g. " +
          "'went to a nice cafe near the post office, loved it, bought a pie for 5.50' — the note AND the " +
          "purchase must both be recorded), " +
          "meta (a conversational message, a complaint, a follow-up ABOUT the assistant or a previous answer, or a " +
          "request about how the bot works — e.g. 'answer in Russian', 'did you save it?', 'why didn't you reply', " +
          "'what is it called in the database'), " +
          "escalate (the user wants a NEW capability this assistant does NOT have, or is dissatisfied it cannot do " +
          "something — a feature request / 'can you also do X' / 'this should also…' / 'I want it to…' beyond notes, " +
          "reminders and finance), " +
          "or unclear (a real note/request whose action is genuinely ambiguous). No other text.",
        `${historyBlock(hist)}NEW MESSAGE: ${text.slice(0, 1000)}`,
        6,
      );
      // finance-query BEFORE finance so the longer token wins the match; compound before finance too.
      let word = (ans.toLowerCase().match(/save|remind|recall|finance-query|compound|finance|escalate|meta|photo|delete|unclear/) ?? [""])[0];
      // Heuristic fallback (step 207.16): an OpenAI outage used to turn EVERY message — including a
      // crystal-clear "запомни…" — into the unclear-buttons picker (owner test, 19:57–23:01). When the
      // model failed ("" = API error) or shrugged, route obvious phrasings by keyword instead; only a
      // genuinely ambiguous message still gets the picker. Works with the key down (self-sufficiency).
      if (!word || word === "unclear") {
        const h = heuristicIntent(text);
        if (h) word = h;
      }
      // Compound (step 207.18, owner case «кафе + пирожок за 5.50»): ONE message = a note AND a money
      // movement — fan out into BOTH existing actions (the diagram already routes classify → summarize
      // AND classify → parse-document; a compound simply takes both edges). The photo (if any) rides on
      // the finance entry only — parse-document dedupes registration by file id anyway.
      if (word === "compound") {
        out.push({ intent: "save", projectSlug: PROJECT_SLUG, payload: text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date });
        out.push({ intent: "finance", projectSlug: PROJECT_SLUG, payload: text, hookPhrase: "", chatId: msg.chatId, messageId: msg.messageId, date: msg.date, photoFileId: msg.photoFileId });
        continue;
      }
      if (!word || word === "unclear") { intent = "ignore"; unclear = true; }
      else intent = word as Intent;
    }
    out.push({
      intent,
      projectSlug: PROJECT_SLUG,
      // unclear keeps the text so reply-in-telegram can offer buttons; a plain empty message stays "".
      payload: intent === "ignore" ? (unclear ? text : "") : text,
      hookPhrase: "",
      unclear,
      chatId: msg.chatId,
      messageId: msg.messageId,
      date: msg.date,
      // The photo rides along whatever the intent (step 207.18): parse-document registers EVERY incoming
      // image into the registry; the record path links it afterwards (link-images node).
      photoFileId: msg.photoFileId,
    });
  }
  return out;
}

// node:summarize-message — Detailed message summary (cheap model) [step]
async function summarizeMessage(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const classified = (artifacts["classify-message"] as Classified2[]) ?? [];
  const out: WithSummary[] = [];
  for (const c of classified) {
    if (c.intent !== "save" && c.intent !== "remind") continue;
    let summary = await cheapModel(
      "Summarize the user's note concisely but completely, preserving key facts, names and dates. " +
        "Reply with only the summary, one short paragraph.",
      c.payload.slice(0, 4000),
      200,
    );
    if (!summary) summary = c.payload.slice(0, 280); // graceful degrade
    out.push({ ...c, summary });
  }
  return out;
}

// node:search-memory-recall — Find an answer in the notes (LightRAG query) [step]
async function searchMemoryRecall(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const classified = (artifacts["classify-message"] as Classified2[]) ?? [];
  const out: RecallAnswer[] = [];
  for (const c of classified) {
    if (c.intent !== "recall") continue;
    // Finance-ledger bridge (step 207.16): the vector memory never holds money rows, so BEFORE asking
    // LightRAG also match the question against automation_finance — a recall that mentions a purchase,
    // a person who returned money, or an exact amount gets the real ledger rows in the answer.
    const finHits = await searchFinanceLedger(c.payload);
    const wantsImage = /фото|картин|изображ|покажи|чек|receipt|photo|image|picture|show/i.test(c.payload);
    const photoUrls = wantsImage ? finHits.map((h) => h.imageUrl).filter((u): u is string => !!u).slice(0, 2) : [];
    try {
      const r = await fetch(`${RAG_URL}/query`, {
        method: "POST",
        headers: { "Content-Type": "application/json", "X-API-Key": RAG_KEY, "X-Agent-Identity": PROJECT_SLUG },
        // EN augmentation (207.20c): the vector docs are English — a non-English question searches
        // noticeably better with its English keywords alongside (cached translation, free on repeats).
        body: JSON.stringify({ query: `${c.payload}${await queryToEnglish(c.payload).then((en) => (en ? ` (${en})` : ""))}`, mode: "hybrid" }),
        signal: AbortSignal.timeout(60_000),
      });
      const lang = await getLang(c.chatId);
      if (!r.ok) {
        const fallback = finHits.length
          ? formatFinanceHits(finHits)
          : "Memory is temporarily unavailable, please try again later.";
        out.push({ chatId: c.chatId, query: c.payload, hookPhrase: c.hookPhrase, answer: await toLang(fallback, lang), error: `HTTP ${r.status}`, photoUrls });
        continue;
      }
      const data = (await r.json()) as { response?: string };
      let answer = (data.response ?? "").trim();
      // Clean the raw LightRAG output (step 207.10 P4): drop the "### References" section and internal
      // record tags (Project/Hook/Record #) so the reply never leaks the DB schema, then answer in the
      // user's language (LightRAG's own language is not controllable — re-phrase to the chat's language).
      answer = answer.split(/\n#{1,6}\s*References\b/i)[0].trim();
      answer = answer
        .replace(/^\s*[-*]?\s*\**(Проект|Project|Хук|Hook|Запись|Record)\**\s*[:#].*$/gim, "")
        .replace(/\n{3,}/g, "\n\n")
        .trim();
      // Reverse-link resolution (step 207.16/207.18, scale contract): a memory answer citing records
      // carries [fin#<id>] / [note#<id>] markers — resolve the rows in the local DB (O(1) at ANY ledger
      // size) and surface their photos via record_images when the question asks to see them; the marker
      // itself never reaches the user.
      const citedFin = [...answer.matchAll(/\[fin#(\d+)\]/g)].map((m) => Number(m[1])).filter((n) => n > 0).slice(0, 5);
      const citedNotes = [...answer.matchAll(/\[note#(\d+)\]/g)].map((m) => Number(m[1])).filter((n) => n > 0).slice(0, 5);
      answer = answer.replace(/\s*\[(?:fin|note)#\d+\]\s*/g, " ").replace(/ {2,}/g, " ").trim();
      if (wantsImage) {
        for (const [kind, ids] of [["finance", citedFin], ["note", citedNotes]] as const) {
          for (const id of ids) {
            const linked = await imagesOfRecord(kind, id);
            for (const img of linked) {
              if (!photoUrls.includes(img.url) && photoUrls.length < 3) photoUrls.push(img.url);
            }
            // Legacy fallback: pre-207.18 finance rows carry image_url only.
            if (kind === "finance" && !linked.length) {
              try {
                const row = (await db.prepare("SELECT image_url FROM automation_finance WHERE id = ? AND project = ?").get(id, PROJECT_SLUG)) as { image_url?: string } | null;
                if (row?.image_url && !photoUrls.includes(row.image_url) && photoUrls.length < 3) photoUrls.push(row.image_url);
              } catch { /* best-effort */ }
            }
          }
        }
      }
      // A "nothing found" memory answer is REPLACED by the ledger hits; a real answer gets them appended.
      const ragEmpty = !answer || /no relevant context|не найдено релевант|nothing found|недостаточно информации|нет информации/i.test(answer);
      let clean: string;
      if (finHits.length && ragEmpty) clean = formatFinanceHits(finHits);
      else if (finHits.length) clean = `${answer}\n\n${formatFinanceHits(finHits)}`;
      else clean = answer || `Nothing found in your notes for: ${c.payload}`;
      // Grounding (step 207.20c, the chicken case: «жареная» was invented). Collect the cited records'
      // OWN facts — row text, photo DESCRIPTIONS (automation_images), places — and compose the final
      // answer STRICTLY from them: a detail the facts don't state gets an honest "not recorded", never
      // a guess. Cost-neutral: this call REPLACES the toLang re-phrase.
      const ground: string[] = [];
      const geoForAnswer: GeoMark[] = [];
      for (const [kind, ids] of [["finance", citedFin], ["note", citedNotes]] as const) {
        for (const id of ids) {
          try {
            if (kind === "finance") {
              const row = (await db.prepare("SELECT kind, amount, summary FROM automation_finance WHERE id = ? AND project = ?").get(id, PROJECT_SLUG)) as { kind?: string; amount?: number; summary?: string } | null;
              if (row) ground.push(`finance#${id}: ${row.kind} ${row.amount} — ${row.summary}`);
            } else {
              const row = (await db.prepare("SELECT summary, full_text FROM telegram_notes WHERE id = ?").get(id)) as { summary?: string; full_text?: string } | null;
              if (row) ground.push(`note#${id}: ${row.full_text || row.summary}`);
            }
          } catch { /* row lookup best-effort */ }
          for (const img of await imagesOfRecord(kind, id)) {
            if (img.description) ground.push(`photo attached to ${kind}#${id} shows: ${img.description}`);
          }
          const geos = await geoOfRecord(kind, id);
          if (geos.length) {
            ground.push(`location of ${kind}#${id}: ${geos.map((g) => g.label || `${g.lat},${g.lng}`).join("; ")}`);
            geoForAnswer.push(...geos);
          }
        }
      }
      let final = "";
      if (ground.length) {
        final = await cheapModel(
          `You answer a question about the user's saved records. Use ONLY the facts below (record texts, photo descriptions, locations). If the question asks about a detail the facts do not state, say plainly that the records don't specify it — NEVER guess or embellish. Answer in ${lang}, concise.`,
          `Question: ${c.payload}\nDraft answer (may contain unsupported claims — trust the facts, not it): ${clean.slice(0, 1500)}\nFacts:\n${ground.join("\n").slice(0, 3000)}`,
          400,
        );
      }
      if (!final) final = await toLang(clean, lang);
      // Places of the cited records (step 207.20d): «напомни где я ел пирожки» → tappable maps link.
      if (geoForAnswer.length) final += `\n${geoLines(geoForAnswer.slice(0, 3))}`;
      out.push({
        chatId: c.chatId,
        query: c.payload,
        hookPhrase: c.hookPhrase,
        answer: final,
        photoUrls,
      });
    } catch (e) {
      const lang = await getLang(c.chatId);
      const fallback = finHits.length ? formatFinanceHits(finHits) : "Memory is temporarily unavailable, please try again later.";
      out.push({ chatId: c.chatId, query: c.payload, hookPhrase: c.hookPhrase, answer: await toLang(fallback, lang), error: e instanceof Error ? e.message : String(e), photoUrls });
    }
  }
  return out;
}

// node:parse-document — Digitize a receipt/document or a voice finance note into a money record [step]
// Step 205 §E (finance table + presets, step 207): a photo → save to media store + vision-transcribe; a
// finance text → cheap-model extract. The model segments into preset categories (multi-flag). The row is
// written to the SEPARATE automation_finance ledger (NOT telegram_notes — its finance columns are
// deprecated). The goal is a usable voice/photo finance ledger, not perfect books.
type FinanceRow = {
  dbId: number | null; chatId: string; kind: "income" | "expense"; amount: number; categories: string[];
  summary: string; imageUrl: string | null; duplicateOfId?: number | null;
  // Photo↔record linking outcomes (step 207.16/207.18, owner "editing state" case):
  attachedToId?: number | null;   // this photo was linked to an EXISTING record (no new row)
  attachedKind?: "note" | "finance"; // which kind of record the photo attached to
  pendingSaved?: boolean;         // photo parked for the NEXT record (no target yet)
  pendingAttached?: boolean;      // a previously parked photo was linked to THIS new row
  sourceText?: string;            // the original message text (207.18d — companion-note material)
};
async function parseDocument(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const classified = (artifacts["classify-message"] as Classified2[]) ?? [];
  const out: FinanceRow[] = [];

  // Phase 1 (rule R2): register EVERY incoming photo — whatever the intent — as a first-class row
  // (media upload + ONE vision call giving description AND, for receipts, the itemized extraction).
  // Dedupe by file id: a compound message fans into two classified entries sharing the photo.
  const analyzed = new Map<string, { imageId: number | null; url: string | null; vision: VisionAnalysis }>();
  for (const c of classified) {
    if (!c.photoFileId || analyzed.has(c.photoFileId)) continue;
    let url: string | null = null;
    let vision: VisionAnalysis = { description: "", finance: null };
    const photo = await fetchTelegramPhoto(c.photoFileId);
    if (photo) {
      url = await uploadToMedia(photo.bytes);
      vision = await visionAnalyze(photo.dataUrl, c.payload);
    }
    // Typed routing (207.18d): a receipt/invoice = 'document' (finance-bound); everything else =
    // 'photo' (note-bound) — the financial and informational planes never mix.
    const kindHint: "document" | "photo" = vision.finance ? "document" : "photo";
    const imageId = url ? await registerImage(c.chatId, url, vision.description, kindHint) : null;
    analyzed.set(c.photoFileId, { imageId, url, vision });
    // Polite geo fallback (step 207.20, owner decision): a PLACE-worthy photo (not a receipt) arrived
    // and the chat has no pending location — tell the user photos never carry coordinates (Bot API
    // strips EXIF) and how to add the place. Once per photo; receipts are money, not places.
    if (imageId != null && kindHint === "photo" && !(await loadPendingGeo(c.chatId)).length) {
      await sendLocalized(
        c.chatId,
        "Note: photos don't carry location data in Telegram. If you'll want to FIND this place later, send a location (attach → Location) or a Google Maps link in the next message — I'll pin it to this record.",
      );
    }
  }

  // Phase 2: the finance action itself.
  for (const c of classified) {
    if (c.intent !== "finance") continue;
    const reg = c.photoFileId ? analyzed.get(c.photoFileId) : undefined;
    const imageUrl = reg?.url ?? null;
    let extract: FinanceExtract | null = reg ? reg.vision.finance : null;
    // A captioned photo whose IMAGE has no readable amount may still carry the amount in the WORDS
    // («пирожок за 5.50» под фото) — try the text; a plain text message is the text path as before.
    if (!extract && c.payload.trim()) extract = await textExtractFinance(c.payload);

    if (!extract) {
      if (imageUrl && reg?.imageId != null) {
        // No money in this photo/words. «обнови/добавь/прикрепи/этот…» → attach to the latest recent
        // record WITHOUT images of ANY kind (rule R5) + re-index its vector doc (rule R7); a bare photo
        // right after such a record attaches too (shorter window). Otherwise it stays pending (forever
        // in the registry; auto-attach window only bounds silent gluing).
        const target = wantsAttach(c.payload)
          ? await latestRecordWithoutImages(c.chatId)
          : await latestRecordWithoutImages(c.chatId, 900);
        if (target) {
          await linkImages(target.kind, target.id, [{ id: reg.imageId, url: imageUrl, description: reg.vision.description, kindHint: reg.vision.finance ? "document" : "photo" }]);
          await reingestRecord(target.kind, target.id);
          out.push({ dbId: target.id, chatId: c.chatId, kind: "expense", amount: 0, categories: [], summary: target.summary, imageUrl, attachedToId: target.id, attachedKind: target.kind });
          continue;
        }
        out.push({ dbId: null, chatId: c.chatId, kind: "expense", amount: 0, categories: [], summary: "", imageUrl, pendingSaved: true });
        continue;
      }
      // Text without an amount and without a photo — reply-in-telegram surfaces the miss.
      out.push({ dbId: null, chatId: c.chatId, kind: "expense", amount: 0, categories: [], summary: "", imageUrl });
      continue;
    }

    // Write to the SEPARATE finance ledger (step 207 owner decision). categories = validated preset ids
    // (multi-flag) stored as a JSON array. image_url keeps the FIRST photo for back-compat readers; the
    // authoritative photo set is record_images (rule R3, linked in the link-images node / right here for
    // the record's own receipt photo). Vector ingest moved to the ingest node (after linking — rule R4).
    // Owner rule (207.18d): the finance row carries ONLY document images (receipts). A non-document
    // photo (the pie itself) stays pending here — link-images routes it into the informational plane
    // (a note; created as a companion if this message was purely financial). image_url (back-compat
    // display) is set only for a document photo.
    const isDoc = reg?.vision.finance != null;
    const res = await db
      .prepare(
        "INSERT INTO automation_finance (project, kind, amount, categories, summary, image_url, chat_id, msg_date) " +
          "VALUES (?, ?, ?, ?, ?, ?, ?, ?)",
      )
      .run(PROJECT_SLUG, extract.kind, extract.amount, JSON.stringify(extract.categories), extract.summary, isDoc ? imageUrl : null, c.chatId, c.date);
    const dbId = Number((res as { lastInsertRowid?: number | bigint }).lastInsertRowid ?? 0) || null;
    if (dbId != null && reg?.imageId != null && imageUrl && isDoc) {
      await linkImages("finance", dbId, [{ id: reg.imageId, url: imageUrl, description: reg.vision.description, kindHint: "document" }]);
    }
    // Soft duplicate check (step 207.x, Tier 2): a recent identical money movement from this chat → the
    // record is kept, but reply-in-telegram offers a one-tap "keep both / remove duplicate".
    const duplicateOfId = dbId != null
      ? await findRecentDuplicateFinance(dbId, extract.kind, extract.amount, extract.summary, c.chatId)
      : null;
    out.push({ dbId, chatId: c.chatId, kind: extract.kind, amount: extract.amount, categories: extract.categories, summary: extract.summary, imageUrl, duplicateOfId, sourceText: c.payload });
  }
  return out;
}

// node:persist-note-to-db — Write the note/reminder to the project DB [step]
// Writes one Record per save / remind / recall (188-R). Each row carries hook_phrase (the
// matched phrase) and condition (the declared-guard outcome) — the ontology trace: what the
// user said → what matched → what ran.
async function persistNoteToDb(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const withSummary = (artifacts["summarize-message"] as WithSummary[]) ?? [];
  const recalls = (artifacts["search-memory-recall"] as RecallAnswer[]) ?? [];
  const out: Persisted[] = [];

  for (const item of withSummary) {
    // A plain note to remember — one row, no reminder/event time.
    if (item.intent === "save") {
      const res = await db
        .prepare(
          "INSERT INTO telegram_notes (project_slug, hook_action, hook_phrase, condition, chat_id, msg_date, reminder_due, event_at, full_text, summary) " +
            "VALUES (?, 'save', ?, NULL, ?, ?, NULL, NULL, ?, ?)",
        )
        .run(PROJECT_SLUG, item.hookPhrase, item.chatId, item.date, item.payload, item.summary);
      const dbId = Number((res as { lastInsertRowid?: number | bigint }).lastInsertRowid ?? 0) || null;
      // Soft duplicate check (step 207.x, Tier 2): a recent identical saved note from this chat.
      const duplicateOfId = dbId != null
        ? await findRecentDuplicateNote(dbId, item.summary, item.chatId)
        : null;
      out.push({ dbId, intent: "save", reminderDue: null, eventAt: null, needsWhen: false, chatId: item.chatId, summary: item.summary, payload: item.payload, duplicateOfId });
      continue;
    }

    // A reminder message — step 207 event+reminder model: extract EVERY reminder in the message (an array),
    // each with an event time + a notify time, and write ONE row per timed reminder.
    const specs = await parseReminders(item.payload, item.summary);
    const timed = specs
      .map((s) => ({ ...s, reminderDue: isoToUnix(s.reminderISO), eventAt: isoToUnix(s.eventISO) }))
      .filter((s) => s.reminderDue != null);

    if (!timed.length) {
      // No parseable notify time anywhere in the message — ask "when?" (no row persisted yet).
      out.push({ dbId: null, intent: "remind", reminderDue: null, eventAt: null, needsWhen: true, chatId: item.chatId, summary: item.summary, payload: item.payload });
      continue;
    }

    for (const s of timed) {
      const res = await db
        .prepare(
          "INSERT INTO telegram_notes (project_slug, hook_action, hook_phrase, condition, chat_id, msg_date, reminder_due, event_at, full_text, summary) " +
            "VALUES (?, 'remind', ?, 'date-parsed', ?, ?, ?, ?, ?, ?)",
        )
        .run(PROJECT_SLUG, item.hookPhrase, item.chatId, item.date, s.reminderDue, s.eventAt, item.payload, s.summary);
      const dbId = Number((res as { lastInsertRowid?: number | bigint }).lastInsertRowid ?? 0) || null;
      out.push({ dbId, intent: "remind", reminderDue: s.reminderDue, eventAt: s.eventAt, needsWhen: false, chatId: item.chatId, summary: s.summary, payload: item.payload });

      // Best-effort: mirror this timed reminder into the owner's external calendar (step 207 Phase F). The
      // event time is event_at (falls back to the notify time if the message gave only one). Inert without
      // Google creds / an unconnected calendar → no-op; wrapped so it NEVER breaks the saved reminder.
      try {
        const eventTime = s.eventAt ?? s.reminderDue;
        if (dbId != null && eventTime != null) {
          const extId = await pushCalendarEvent({
            eventAt: eventTime,
            reminderDue: s.reminderDue,
            summary: s.summary || item.summary,
            description: item.payload,
          });
          if (extId) {
            await db.prepare("UPDATE telegram_notes SET external_event_id = ? WHERE id = ?").run(extId, dbId);
          }
        }
      } catch {
        /* calendar push is best-effort — the reminder is already saved */
      }
    }
  }

  // Recall is ALSO a Record (188-R): every memory search becomes a "request" row so it shows
  // in the universal table. full_text = the question; summary = question + a digest of the answer.
  for (const rc of recalls) {
    const digest = rc.answer.replace(/\s+/g, " ").slice(0, 200);
    const summary = `Q: ${rc.query} — A: ${digest}`;
    await db
      .prepare(
        "INSERT INTO telegram_notes (project_slug, hook_action, hook_phrase, condition, chat_id, reminder_due, full_text, summary) " +
          "VALUES (?, 'recall', ?, NULL, ?, NULL, ?, ?)",
      )
      .run(PROJECT_SLUG, rc.hookPhrase, rc.chatId, rc.query, summary);
  }

  return out;
}

// node:link-images — Link this run's images to this run's records (many-to-many) [step]
// Step 207.18 (rules R3/R5): the pending images of a chat attach to EVERY record born from the same
// message burst — the owner's cafe case: interior photo + receipt photo + a compound text → the note AND
// the finance record BOTH carry BOTH photos. The finance record's own receipt photo was already linked
// at creation (parse-document); this node links what arrived photo-first. Output feeds the ingest node
// so every vector doc mentions its photos (rule R4).
type LinkOutcome = {
  noteImages: Record<number, RegisteredImage[]>;
  financeImages: Record<number, RegisteredImage[]>;
  linked: number;
  // Companion notes born here (207.18d): a purely-financial message with a NON-document photo spawns
  // an informational note (the purchase story + the photo) so the finance table stays money-only.
  companions: Array<{ chatId: string; noteId: number; summary: string }>;
  // Geo-marks (step 207.20): the final place sets per record, mirror of the image maps — the ingest
  // node appends them to the vector docs (geoBlock).
  noteGeo: Record<number, GeoMark[]>;
  financeGeo: Record<number, GeoMark[]>;
};
async function linkImagesNode(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const persisted = (artifacts["persist-note-to-db"] as Persisted[]) ?? [];
  const finances = (artifacts["parse-document"] as FinanceRow[]) ?? [];
  const out: LinkOutcome = { noteImages: {}, financeImages: {}, linked: 0, companions: [], noteGeo: {}, financeGeo: {} };

  // New records of this run, grouped by chat (finance rows keep their source text for companions).
  const byChat = new Map<string, { notes: number[]; fins: Array<{ id: number; summary: string; sourceText: string }> }>();
  for (const p of persisted) {
    if (p.dbId == null || p.intent === "recall") continue;
    const g = byChat.get(p.chatId) ?? { notes: [], fins: [] };
    g.notes.push(p.dbId);
    byChat.set(p.chatId, g);
  }
  for (const f of finances) {
    if (f.dbId == null || f.attachedToId != null) continue;
    const g = byChat.get(f.chatId) ?? { notes: [], fins: [] };
    g.fins.push({ id: f.dbId, summary: f.summary, sourceText: f.sourceText ?? f.summary });
    byChat.set(f.chatId, g);
  }

  for (const [chatId, g] of byChat) {
    const pending = await loadPendingImages(chatId);
    // Typed routing (207.18d, owner rule): documents (receipts) belong to FINANCE records; plain
    // photos (the pie, the interior) belong to NOTES — the two planes never mix.
    const docs = pending.filter((p) => p.kindHint === "document");
    const photos = pending.filter((p) => p.kindHint !== "document");

    // Documents → this run's finance records (fallback: notes, e.g. "запомни этот счёт" without money).
    if (docs.length) {
      const finTargets = g.fins.map((f) => f.id);
      if (finTargets.length) {
        for (const id of finTargets) { await linkImages("finance", id, docs); out.linked += docs.length; }
      } else if (g.notes.length) {
        for (const id of g.notes) { await linkImages("note", id, docs); out.linked += docs.length; }
      }
    }

    // Photos → this run's notes; a purely-financial burst gets a COMPANION note carrying the story
    // and the photo («купил пирожок за 5.50» + фото пирожка → финансы без фото, заметка с фото).
    if (photos.length) {
      let noteTargets = [...g.notes];
      if (!noteTargets.length && g.fins.length) {
        const f = g.fins[0];
        try {
          const res = await db
            .prepare(
              "INSERT INTO telegram_notes (project_slug, hook_action, hook_phrase, condition, chat_id, msg_date, reminder_due, event_at, full_text, summary) " +
                "VALUES (?, 'save', '', NULL, ?, ?, NULL, NULL, ?, ?)",
            )
            .run(PROJECT_SLUG, chatId, Math.floor(Date.now() / 1000), f.sourceText, f.summary);
          const noteId = Number((res as { lastInsertRowid?: number | bigint }).lastInsertRowid ?? 0) || null;
          if (noteId != null) {
            noteTargets = [noteId];
            out.companions.push({ chatId, noteId, summary: f.summary });
          }
        } catch { /* companion is best-effort — the photo stays pending */ }
      }
      for (const id of noteTargets) { await linkImages("note", id, photos); out.linked += photos.length; }
    }

    // Geo-marks (step 207.20): the chat's pending places attach to EVERY record born from this burst —
    // BOTH kinds (a place belongs to the visit note AND the purchase made there), companion notes included.
    const pendingGeo = await loadPendingGeo(chatId);
    if (pendingGeo.length) {
      const noteIds = new Set<number>([...g.notes, ...out.companions.filter((c) => c.chatId === chatId).map((c) => c.noteId)]);
      for (const id of noteIds) { await linkGeo("note", id, pendingGeo); out.linked += pendingGeo.length; }
      for (const f of g.fins) { await linkGeo("finance", f.id, pendingGeo); out.linked += pendingGeo.length; }
    }

    // Companion notes are born AFTER the ingest queue was formed — ingest them right here (rule R4),
    // AFTER geo linking so their vector doc carries the place (207.20).
    for (const c of out.companions.filter((x) => x.chatId === chatId)) {
      await ingestNoteDocToMemory(c.noteId, g.fins[0]?.sourceText ?? c.summary, await imagesOfRecord("note", c.noteId), await geoOfRecord("note", c.noteId));
    }

    // Expose the final image and geo sets (own receipts included) for the ingest node.
    for (const f of g.fins) {
      out.financeImages[f.id] = await imagesOfRecord("finance", f.id);
      out.financeGeo[f.id] = await geoOfRecord("finance", f.id);
    }
    for (const id of g.notes) {
      out.noteImages[id] = await imagesOfRecord("note", id);
      out.noteGeo[id] = await geoOfRecord("note", id);
    }
  }
  return out;
}

// node:ingest-note-to-memory — Write this run's records to vector memory (LightRAG) [step]
// Step 207.18 (rule R4): EVERY record of EVERY kind gets a vector doc with its reverse marker
// ([note#id] / [fin#id]) and the vision descriptions of its linked photos. Runs AFTER link-images so
// the doc already knows its photos; a later attach re-ingests via reingestRecord (rule R7).
async function ingestNoteToMemory(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const persisted = (artifacts["persist-note-to-db"] as Persisted[]) ?? [];
  const finances = (artifacts["parse-document"] as FinanceRow[]) ?? [];
  const links = (artifacts["link-images"] as LinkOutcome) ?? { noteImages: {}, financeImages: {}, linked: 0, companions: [], noteGeo: {}, financeGeo: {} };
  const out: Ingested[] = [];
  for (const p of persisted) {
    if (p.dbId == null) continue; // needs-when rows are not yet stored
    try {
      const ok = await ingestNoteDocToMemory(p.dbId, p.payload, links.noteImages[p.dbId] ?? [], links.noteGeo?.[p.dbId] ?? []);
      out.push({ dbId: p.dbId, chatId: p.chatId, summary: p.summary, ingestOk: ok, error: ok ? undefined : "ingest failed" });
    } catch (e) {
      out.push({ dbId: p.dbId, chatId: p.chatId, summary: p.summary, ingestOk: false, error: e instanceof Error ? e.message : String(e) });
    }
  }
  // Finance rows created this run (attach-only outcomes re-ingest inside parse-document instead).
  for (const f of finances) {
    if (f.dbId == null || f.attachedToId != null) continue;
    try {
      await ingestFinanceToMemory(f.dbId, f.kind, f.amount, f.categories, f.summary, links.financeImages[f.dbId] ?? [], links.financeGeo?.[f.dbId] ?? []);
    } catch { /* best-effort — the ledger row is already saved */ }
  }
  return out;
}

// node:answer-finance-query — Aggregate the finance ledger to answer a money question [step]
// Step 207.8: a finance-query message (e.g. "how much did I spend on food this week") → parse filters
// (kind / categories / window) → SUM over automation_finance → a plain-text answer back to the chat.
type FinanceAnswer = { chatId: string; answer: string };
async function answerFinanceQuery(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const classified = (artifacts["classify-message"] as Classified2[]) ?? [];
  const out: FinanceAnswer[] = [];
  for (const c of classified) {
    if (c.intent !== "finance-query") continue;
    const q = await parseFinanceQuery(c.payload);
    // Reconciliation / break-even question (step 207.10 P2): open the button-driven wizard instead of a
    // naive one-shot sum. The wizard sends its own messages; produce no FinanceAnswer for this message.
    if (q.reconcile) { await reconAskPeriod(c.chatId); continue; }
    // Point lookup FIRST (step 207.16): «Петя возвращал долг?» / «сколько стоила курица» names specific
    // records — keyword-match the ledger and answer with the actual rows, not a blind aggregate.
    const hits = await searchFinanceLedger(c.payload, 5);
    if (hits.length) {
      const lang = await getLang(c.chatId);
      // Places of the matched rows (step 207.20d), appended AFTER translation — URLs stay raw.
      const geoTails: string[] = [];
      for (const h of hits.slice(0, 3)) {
        const geos = await geoOfRecord("finance", h.id);
        if (geos.length) geoTails.push(geoLines(geos.slice(0, 1)));
      }
      const tail = geoTails.length ? `\n${geoTails.join("\n")}` : "";
      out.push({ chatId: c.chatId, answer: (await toLang(formatFinanceHits(hits), lang)) + tail });
      continue;
    }
    try {
      const clauses = ["project = ?"];
      const params: Array<string | number> = [PROJECT_SLUG];
      if (q.kind) { clauses.push("kind = ?"); params.push(q.kind); }
      if (q.sinceDays) { clauses.push("created_at >= ?"); params.push(Math.floor(Date.now() / 1000) - q.sinceDays * 86400); }
      const rows = (await db
        .prepare(`SELECT kind, amount, categories FROM automation_finance WHERE ${clauses.join(" AND ")}`)
        .all(...params)) as Array<{ kind: string; amount: number; categories: string }>;
      // Category filter is applied in the app layer (categories is a JSON array — multi-flag).
      const filtered = q.categories.length
        ? rows.filter((r) => parseCategoryIds(r.categories).some((id) => q.categories.includes(id)))
        : rows;
      const catLabel = q.categories.map((id) => categoryLabel(id, "en")).join(", ");
      const kindLabel = q.kind ?? "money";
      const periodLabel = q.sinceDays ? `in the last ${q.sinceDays} day(s)` : "in total";
      // Honest arithmetic (step 207.16): a single-sided question sums that side; a BOTH-sides question
      // must never add income and expense together into one number ("158.5 total" bug) — it reports
      // income / expense / net separately.
      let answer: string;
      if (!filtered.length) {
        answer = `No ${kindLabel} records${catLabel ? ` for ${catLabel}` : ""} ${periodLabel}.`;
      } else if (q.kind) {
        const total = filtered.reduce((s, r) => s + Number(r.amount || 0), 0);
        answer = `${kindLabel}${catLabel ? ` for ${catLabel}` : ""} ${periodLabel}: ${total} across ${filtered.length} record(s).`;
      } else {
        const income = filtered.filter((r) => r.kind === "income").reduce((s, r) => s + Number(r.amount || 0), 0);
        const expense = filtered.filter((r) => r.kind !== "income").reduce((s, r) => s + Number(r.amount || 0), 0);
        answer = `Money${catLabel ? ` for ${catLabel}` : ""} ${periodLabel}: income ${income} · expense ${expense} · net ${income - expense}, across ${filtered.length} record(s).`;
      }
      const lang = await getLang(c.chatId);
      out.push({ chatId: c.chatId, answer: await translateCached(answer.charAt(0).toUpperCase() + answer.slice(1), lang) });
    } catch {
      out.push({ chatId: c.chatId, answer: await translateCached("I couldn't read your finance ledger just now — please try again.", await getLang(c.chatId)) });
    }
  }
  return out;
}

// node:reply-in-telegram — Reply to the user in Telegram [step]
async function replyInTelegram(artifacts: Record<string, unknown>): Promise<unknown> {
  "use step";

  const persisted = (artifacts["persist-note-to-db"] as Persisted[]) ?? [];
  const ingested = (artifacts["ingest-note-to-memory"] as Ingested[]) ?? [];
  const recalls = (artifacts["search-memory-recall"] as RecallAnswer[]) ?? [];
  const classified = (artifacts["classify-message"] as Classified2[]) ?? [];
  const finances = (artifacts["parse-document"] as FinanceRow[]) ?? [];
  const financeAnswers = (artifacts["answer-finance-query"] as FinanceAnswer[]) ?? [];
  const linkOut = (artifacts["link-images"] as LinkOutcome) ?? { noteImages: {}, financeImages: {}, linked: 0, companions: [] };
  const ingestById = new Map(ingested.map((i) => [i.dbId, i]));

  const errors: string[] = [];
  let saved = 0;
  let reminded = 0;
  let answered = 0;
  let finance = 0;

  for (const p of persisted) {
    try {
      if (p.needsWhen) {
        await sendLocalized(p.chatId, "When should I remind you? Reply with a date and time (for example: today at 19:05).");
        continue;
      }
      if (p.intent === "save") {
        const ing = ingestById.get(p.dbId);
        // Photo count via the link table (step 207.18) — the residence case confirms its photo.
        const nImgs = p.dbId != null ? (linkOut.noteImages[p.dbId] ?? []).length : 0;
        const photoNote = nImgs ? ` · 📷 ${nImgs > 1 ? `${nImgs} photos` : "photo"} attached` : "";
        if (ing && ing.ingestOk) {
          await sendLocalized(p.chatId, `Saved: ${p.summary}${photoNote}`);
          saved++;
        } else {
          await sendLocalized(p.chatId, `Saved${photoNote}, but memory is unavailable — I'll index it later. (${ing?.error ?? "ingest error"})`);
          saved++;
        }
        // Soft-duplicate nudge (step 207.x): the note is already saved — offer a one-tap removal.
        if (p.duplicateOfId && p.dbId != null) {
          await promptDuplicate(p.chatId, "note", p.dbId, p.duplicateOfId, p.summary);
        }
      } else if (p.intent === "remind" && p.reminderDue) {
        const fmt = (t: number) => {
          const d = new Date(t * 1000);
          return `${String(d.getHours()).padStart(2, "0")}:${String(d.getMinutes()).padStart(2, "0")}`;
        };
        let msg = `I'll remind you at ${fmt(p.reminderDue)}: ${p.summary}`;
        // Event+reminder model (step 207): if the thing happens at a different time than the notify, say so.
        if (p.eventAt && p.eventAt !== p.reminderDue) msg += ` (event at ${fmt(p.eventAt)})`;
        await sendLocalized(p.chatId, msg);
        reminded++;
      }
    } catch (e) {
      errors.push(`reply ${p.dbId ?? "?"}: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  for (const rc of recalls) {
    try {
      await tgSend(rc.chatId, rc.answer);
      // Finance-ledger bridge (step 207.16): the question asked to SEE a receipt and matched rows carry
      // photos → send them right after the text answer (best-effort, the answer already went out).
      for (const u of rc.photoUrls ?? []) {
        try { await tgSendPhotoByUrl(rc.chatId, u, ""); } catch { /* best-effort */ }
      }
      answered++;
    } catch (e) {
      errors.push(`recall reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Finance-query answers (step 207.8): the aggregated money figure back to the chat.
  for (const fa of financeAnswers) {
    try {
      await tgSend(fa.chatId, fa.answer);
      answered++;
    } catch (e) {
      errors.push(`finance-query reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Finance records (step 205 §E): confirm what was digitized (or that nothing readable was found).
  for (const f of finances) {
    try {
      // Photo↔record linking outcomes (step 207.16/207.18, owner "editing state" case) — these are not
      // new records, they are the photo finding its home in either direction of arrival.
      if (f.attachedToId != null) {
        await sendLocalized(f.chatId, `Photo attached to the ${f.attachedKind === "note" ? "note" : "record"}: ${f.summary || `#${f.attachedToId}`}. Ask "покажи фото" to see it.`);
        continue;
      }
      if (f.pendingSaved) {
        // If the SAME run already linked the pending photo to a record (a captioned photo whose caption
        // became a note — the residence case), the record's own confirmation covers it — stay silent.
        if (linkOut.linked > 0) continue;
        await sendLocalized(f.chatId, "Photo saved. Tell me what it is (and the amount if it's a purchase) — I'll record it with this photo attached.");
        continue;
      }
      if (f.dbId == null) {
        await sendLocalized(f.chatId, "I couldn't read a clear amount from that. Try a clearer photo, or tell me the amount.");
        continue;
      }
      const sign = f.kind === "income" ? "+" : "−";
      const cats = f.categories.map((id) => categoryLabel(id, "en")).join(", ");
      const nImgs = (linkOut.financeImages[f.dbId] ?? []).length;
      const photoNote = nImgs ? ` · 📷 receipt attached` : "";
      // Companion note (207.18d): a non-document photo went to the INFORMATIONAL plane — say so.
      const companion = linkOut.companions.find((cn) => cn.chatId === f.chatId);
      const companionNote = companion ? ` · 📝 the photo went to a note: "${companion.summary.slice(0, 60)}"` : "";
      await sendLocalized(f.chatId, `Recorded ${f.kind} ${sign}${f.amount} · ${cats}: ${f.summary}${photoNote}${companionNote}`);
      finance++;
      // Soft-duplicate nudge (step 207.x): the record is already saved — offer a one-tap removal.
      if (f.duplicateOfId && f.dbId != null) {
        await promptDuplicate(f.chatId, "finance", f.dbId, f.duplicateOfId, f.summary);
      }
    } catch (e) {
      errors.push(`finance reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Help / start / digitize-hint (step 205 §I): reply with the capability blurb or the command's hint.
  let helped = 0;
  for (const c of classified) {
    if (c.intent !== "help") continue;
    try {
      await sendLocalized(c.chatId, c.payload || CAPABILITIES);
      helped++;
    } catch (e) {
      errors.push(`help reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Photo return (step 207.10/207.18): "/photo <id>" or plain words → resolve the photo through the
  // record_images link table (rule R6 — the ONLY path; the "most recent photo" fallback is REMOVED:
  // it sent an unrelated chicken receipt for a cafe question).
  for (const c of classified) {
    if (c.intent !== "photo") continue;
    try {
      const id = Number((c.payload.match(/\d+/) ?? [])[0]);
      let found: { id: number; image_url: string; summary: string } | null = null;
      if (Number.isFinite(id) && id > 0) {
        const row = (await db
          .prepare("SELECT image_url, summary FROM automation_finance WHERE id = ? AND project = ?")
          .get(id, PROJECT_SLUG)) as { image_url?: string; summary?: string } | null;
        if (!row) {
          await sendLocalized(c.chatId, `I couldn't find finance record #${id}.`);
          continue;
        }
        const linked = await imagesOfRecord("finance", id);
        const url = linked[0]?.url ?? row.image_url ?? null;
        if (!url) {
          await sendLocalized(c.chatId, `Finance record #${id} has no photo attached.`);
          continue;
        }
        found = { id, image_url: url, summary: row.summary ?? "" };
      } else {
        // Natural-language request: match the words against finance records first, then against the
        // image registry's own vision descriptions («покажи кафе» matches an image described as a cafe).
        const hits = await searchFinanceLedger(c.payload, 10);
        for (const h of hits) {
          const linked = await imagesOfRecord("finance", h.id);
          const url = linked[0]?.url ?? h.imageUrl;
          if (url) { found = { id: h.id, image_url: url, summary: h.summary }; break; }
        }
        if (!found) {
          // Registry pass covers EVERY stored image (note-linked, finance-linked and pending). The
          // descriptions are English → search with the union of the original tokens and the EN translation (207.20c).
          const enQuery = await queryToEnglish(c.payload);
          const tokens = (`${c.payload} ${enQuery}`.toLowerCase().match(/[\p{L}\p{N}]{3,}/gu) ?? []).slice(0, 12);
          if (tokens.length) {
            try {
              const rows = (await db
                .prepare("SELECT id, media_url, description FROM automation_images WHERE project = ? ORDER BY created_at DESC LIMIT 200")
                .all(PROJECT_SLUG)) as Array<{ id: number; media_url: string; description: string }>;
              const hit = rows.find((r) => tokens.some((t) => (r.description || "").toLowerCase().includes(t.slice(0, Math.max(4, t.length - 2)))));
              if (hit) found = { id: hit.id, image_url: hit.media_url, summary: hit.description };
            } catch { /* registry unavailable */ }
          }
        }
        if (!found) {
          // Rule R6: no link, no guessing — an honest miss instead of a random receipt.
          await sendLocalized(c.chatId, "I couldn't find a saved photo matching that. Say more specifically what the photo shows, or use /photo with the record number.");
          continue;
        }
      }
      const row = { image_url: found.image_url, summary: found.summary };
      const sent = await tgSendPhotoByUrl(c.chatId, row.image_url, `${row.summary ?? ""}`.trim());
      if (!sent) await sendLocalized(c.chatId, `I couldn't send the photo for record #${found.id} right now.`);
      // Public link (step 207.10 P6): also send a clickable link on the real domain, verified live — the
      // localized prefix is translated (cached), the raw URL is appended untouched (never localhost).
      const link = publicMediaUrl(row.image_url);
      if (!/localhost|127\.0\.0\.1/.test(link) && (await urlIsLive(link))) {
        const prefix = await translateCached("Open the image:", await getLang(c.chatId));
        await tgSend(c.chatId, `${prefix} ${link}`);
      }
    } catch (e) {
      errors.push(`photo reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Note return (step 207.10 P5): "/note <id>" → send the saved note's full text (+ image link if any)
  // back to the chat — the notes analogue of /photo. Scoped to this project.
  for (const c of classified) {
    if (c.intent !== "note") continue;
    try {
      const lang = await getLang(c.chatId);
      const id = Number((c.payload.match(/\d+/) ?? [])[0]);
      if (!Number.isFinite(id) || id <= 0) {
        await sendLocalized(c.chatId, "Which note? Send /note followed by the record number (for example: /note 3).");
        continue;
      }
      const row = (await db
        .prepare("SELECT full_text, summary, image_url FROM telegram_notes WHERE id = ? AND project_slug = ?")
        .get(id, PROJECT_SLUG)) as { full_text?: string; summary?: string; image_url?: string } | null;
      if (!row) {
        await sendLocalized(c.chatId, `I couldn't find note #${id}.`);
        continue;
      }
      const body = (row.full_text || row.summary || "").trim();
      const header = await translateCached(`Note #${id}:`, lang);
      await tgSend(c.chatId, `${header}\n${body || "—"}`);
      if (row.image_url) {
        const link = publicMediaUrl(row.image_url);
        if (!/localhost|127\.0\.0\.1/.test(link) && (await urlIsLive(link))) {
          const prefix = await translateCached("Attached image:", lang);
          await tgSend(c.chatId, `${prefix} ${link}`);
        }
      }
    } catch (e) {
      errors.push(`note reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Delete requests (step 207.18b): «убери запись», «удали все записи по 11 за пирожки». DESTRUCTIVE →
  // never silent: find the targets (both tables, amounts + keywords), list them, and ask for a one-tap
  // confirmation. The tap comes back as deldo:<specId> (handled in fetch-telegram-updates) and deletes
  // each pair (row + vector doc + image links). Zero matches = an honest miss, nothing is guessed.
  let deletePrompts = 0;
  for (const c of classified) {
    if (c.intent !== "delete") continue;
    try {
      const lang = await getLang(c.chatId);
      const targets = await findDeleteTargets(c.payload, c.chatId);
      if (!targets.length) {
        await sendLocalized(c.chatId, "I couldn't find records matching that. Say what to delete more specifically (words or the amount).");
        continue;
      }
      const specId = crypto.randomUUID().slice(0, 8);
      try {
        await db
          .prepare("INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES (?, ?)")
          .run(`delspec:${specId}`, JSON.stringify({ chatId: c.chatId, targets: targets.map((t) => ({ kind: t.kind, id: t.id })) }));
      } catch { /* state table required for confirm — fall through, the buttons will just expire */ }
      const listing = targets.slice(0, 10).map((t) => `• ${t.label}`).join("\n") + (targets.length > 10 ? `\n… and ${targets.length - 10} more` : "");
      const head = await translateCached(targets.length === 1 ? "Delete this record?" : `Delete these ${targets.length} records?`, lang);
      await tgSendButtons(c.chatId, `${head}\n${listing}`, [
        { text: await translateCached(`🗑 Yes, delete ${targets.length === 1 ? "it" : `all ${targets.length}`}`, lang), data: `deldo:${specId}` },
        { text: await translateCached("Cancel", lang), data: "delcancel" },
      ]);
      deletePrompts++;
    } catch (e) {
      errors.push(`delete prompt: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Meta / conversational messages (step 207.10 P3): a complaint / follow-up / how-does-it-work message
  // gets a helpful reply in the user's language instead of the unclear-buttons fallback.
  let metaReplies = 0;
  for (const c of classified) {
    if (c.intent !== "meta") continue;
    try {
      const lang = await getLang(c.chatId);
      // History (step 207.16): a follow-up like «а повтори на русском ответ» can only be answered with
      // the previous exchange in front of the model — feed the recent thread alongside the message.
      const hist = await loadHistory(c.chatId);
      const answer = await cheapModel(
        `You are a personal notes & finance Telegram assistant. The user sent a conversational or meta message (possibly a follow-up about the previous answer — use the recent conversation to repeat/translate/clarify it). Reply briefly and helpfully in ${lang}, and guide them: they can send a note to save it, ask a question to search their notes, send a receipt or a money note for finance, or use /help for options. Address their message directly. ` +
          `NEVER invent facts about their saved records (what a photo shows, what was bought, where) — if such a detail is not in the conversation below, tell them to ask the question as a search (e.g. "what did I save about…") so the records are actually looked up. ` +
          `If they ask HOW TO CHANGE THE LANGUAGE (or complain that answers come in the wrong language), answer with exactly these steps, translated into ${lang}: ${LANGUAGE_GUIDE}`,
        `${historyBlock(hist)}NEW MESSAGE: ${c.payload.slice(0, 500)}`, 300,
      );
      await tgSend(
        c.chatId,
        answer ||
          (await translateCached(
            "I process each message automatically — send a note to save it, ask a question to find it, or /help for options.",
            lang,
          )),
      );
      metaReplies++;
    } catch (e) {
      errors.push(`meta reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Coder-escalation (step 207.10 P3/10.3): the user wants a capability beyond this automation's settings.
  // Reply with the localized escalation script (open the architecture cockpit, write steps, launch rocket).
  let escalated = 0;
  for (const c of classified) {
    if (c.intent !== "escalate") continue;
    try {
      await tgSend(c.chatId, await toLang(escalationScript(), await getLang(c.chatId)));
      escalated++;
    } catch (e) {
      errors.push(`escalate reply: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Unclear messages (step 205 §D): offer action buttons instead of silently ignoring. The tap comes
  // back as a callback (handled in fetch-telegram-updates) carrying a forced action.
  let prompted = 0;
  for (const c of classified) {
    if (!c.unclear || !c.payload) continue;
    try {
      const pid = await storePending(c.payload, c.chatId);
      const lang = await getLang(c.chatId);
      await tgSendButtons(c.chatId, await translateCached("I'm not sure what to do with that — pick one:", lang), [
        { text: await translateCached("💾 Save it", lang), data: `${pid}:save` },
        { text: await translateCached("⏰ Remind me", lang), data: `${pid}:remind` },
        { text: await translateCached("🔎 Find it", lang), data: `${pid}:recall` },
        { text: await translateCached("✖ Sent by mistake", lang), data: `${pid}:ignore` },
      ]);
      prompted++;
    } catch (e) {
      errors.push(`prompt: ${e instanceof Error ? e.message : String(e)}`);
    }
  }

  // Key-visibility diagnostic (step 207.16): measured here, inside a step, where fs/env access is legal.
  const diag = `key=${!!OPENAI_KEY()} env=${!!process.env.OPENAI_API_KEY} cwd=${process.cwd()}`;
  return { processed: persisted.length + recalls.length + financeAnswers.length + prompted + finance + helped + metaReplies + escalated + deletePrompts, saved, reminded, answered, finance, errors, diag };
}
