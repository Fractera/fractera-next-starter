// ── Language (step 207.10, P1): reply in the user's language. Detect once per real message, persist
// per chat, and translate fixed replies on demand (cached by a stable hash) so the automation stays
// self-sufficient (only the OpenAI key). Model-generated answers instead get "reply in <lang>". ──
// ── Per-chat short conversation history (step 207.16, owner decision): the last ~10 messages feed the
// classifier and the meta-replies so follow-ups («а повтори на русском ответ») make sense. A pause longer
// than HIST_GAP_SEC cuts the thread — stale context is ignored, not spent on (cache/cost discipline:
// history is small, capped, and clear-cut; unambiguous prefixes skip the model entirely). ──
const HIST_MAX = 10;
const HIST_GAP_SEC = 900; // 15 min
type HistEntry = { role: "user" | "bot"; text: string; ts: number };
async function loadHistory(chatId: string): Promise<HistEntry[]> {
  try {
    const row = (await db.prepare("SELECT value FROM telegram_notes_state WHERE key = ?").get(`hist:${chatId}`)) as { value?: string } | null;
    const arr = row?.value ? (JSON.parse(row.value) as HistEntry[]) : [];
    // TTL: a gap > HIST_GAP_SEC inside the log starts a fresh thread; a stale tail drops everything.
    let seg: HistEntry[] = [];
    for (const e of arr) {
      if (seg.length && e.ts - seg[seg.length - 1].ts > HIST_GAP_SEC) seg = [];
      seg.push(e);
    }
    if (seg.length && Math.floor(Date.now() / 1000) - seg[seg.length - 1].ts > HIST_GAP_SEC) seg = [];
    return seg.slice(-HIST_MAX);
  } catch { return []; }
}
async function appendHistory(chatId: string, role: "user" | "bot", text: string): Promise<void> {
  if (!text) return;
  try {
    const row = (await db.prepare("SELECT value FROM telegram_notes_state WHERE key = ?").get(`hist:${chatId}`)) as { value?: string } | null;
    const arr = row?.value ? (JSON.parse(row.value) as HistEntry[]) : [];
    arr.push({ role, text: text.slice(0, 300), ts: Math.floor(Date.now() / 1000) });
    await db
      .prepare("INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES (?, ?)")
      .run(`hist:${chatId}`, JSON.stringify(arr.slice(-HIST_MAX)));
  } catch { /* state table optional */ }
}
function historyBlock(hist: HistEntry[]): string {
  if (!hist.length) return "";
  return "Recent conversation (oldest first):\n" + hist.map((e) => `${e.role}: ${e.text.slice(0, 160)}`).join("\n") + "\n\n";
}

async function getLang(chatId: string): Promise<string> {
  try {
    const row = (await db
      .prepare("SELECT value FROM telegram_notes_state WHERE key = ?")
      .get(`lang:${chatId}`)) as { value?: string } | null;
    if (row?.value) return row.value;
  } catch { /* default below */ }
  return "English";
}
async function detectLang(text: string): Promise<string> {
  const t = text.trim();
  if (!t) return "";
  if (/[Ѐ-ӿ]/.test(t)) return "Russian"; // fast path (no model call) for the common case
  const ans = await cheapModel(
    "Detect the language of the user's message. Reply with ONLY the English name of the language (e.g. English, Russian, Spanish). No other text.",
    t.slice(0, 200), 4,
  );
  return (ans || "").replace(/[^A-Za-z ]/g, "").trim() || "English";
}
// Detect a real message's language and remember it; terse/empty messages keep the last known language.
async function resolveLang(chatId: string, text: string): Promise<string> {
  const detected = await detectLang(text);
  if (!detected) return getLang(chatId);
  try {
    await db.prepare("INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES (?, ?)").run(`lang:${chatId}`, detected);
  } catch { /* best-effort */ }
  return detected;
}
function hashStr(s: string): string {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) | 0;
  return (h >>> 0).toString(36);
}
// Translate a fixed English reply into the target language (cached), preserving numbers/names/emoji/ids.
async function translateCached(enText: string, lang: string): Promise<string> {
  if (!enText || !lang || /^english$/i.test(lang)) return enText;
  const key = `tr:${lang}:${hashStr(enText)}`;
  try {
    const row = (await db.prepare("SELECT value FROM telegram_notes_state WHERE key = ?").get(key)) as { value?: string } | null;
    if (row?.value) return row.value;
  } catch { /* translate below */ }
  const out = await cheapModel(
    `Translate the following UI message into ${lang}. Keep numbers, names, currency symbols, emoji and any /commands or #ids EXACTLY. Reply with ONLY the translation, no quotes.`,
    enText.slice(0, 900), 400,
  );
  const translated = out || enText;
  try {
    await db.prepare("INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES (?, ?)").run(key, translated);
  } catch { /* best-effort cache */ }
  return translated;
}
// Send a fixed English reply localized to the chat's language (one wrapper over tgSend so every canned
// string becomes multilingual without a per-string table).
// Query → English search keywords (step 207.20c, the chicken case): image descriptions and finance
// summaries from vision are ENGLISH; a query in any other language must match them. One cheap call,
// cached forever by text hash (repeat questions are free). ASCII-only queries skip the call.
async function queryToEnglish(text: string): Promise<string> {
  const t = text.trim();
  if (!t || /^[\x00-\x7F]*$/.test(t)) return "";
  const key = `qen:${hashStr(t)}`;
  try {
    const row = (await db.prepare("SELECT value FROM telegram_notes_state WHERE key = ?").get(key)) as { value?: string } | null;
    if (row?.value) return row.value;
  } catch { /* cache miss */ }
  const out = await cheapModel(
    "Translate the user's search request into ENGLISH keywords for matching against English record descriptions. Reply with ONLY the keywords (3-8 lowercase words, no punctuation).",
    t.slice(0, 300),
    60,
  );
  const en = out.toLowerCase().replace(/[^\x20-\x7E]/g, " ").replace(/\s+/g, " ").trim();
  if (en) {
    try {
      await db.prepare("INSERT OR REPLACE INTO telegram_notes_state (key, value) VALUES (?, ?)").run(key, en);
    } catch { /* cache is best-effort */ }
  }
  return en;
}

