function parseFinanceJson(raw: string): FinanceExtract | null {
  try {
    const m = raw.match(/\{[\s\S]*\}/);
    if (!m) return null;
    const o = JSON.parse(m[0]) as { kind?: string; amount?: unknown; categories?: unknown; summary?: unknown };
    const kind = o.kind === "income" ? "income" : "expense";
    const amount = Number(o.amount);
    if (!Number.isFinite(amount) || amount <= 0) return null;
    // Validate the model's ids against the preset for this kind (multi-flag); empty → the kind's "other_*".
    const categories = normalizeCategories(kind, o.categories);
    return { kind, amount, categories, summary: String(o.summary ?? "").slice(0, 200) };
  } catch {
    return null;
  }
}

